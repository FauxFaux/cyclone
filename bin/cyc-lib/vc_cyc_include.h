/* Built-in Run-time Checks and company */
static void * _check_null(void *ptr) {
  if (ptr == 0) _throw_null();
  return ptr;
}

static void *_check_known_subscript_null(char *ptr,unsigned bound,unsigned elt_sz,
                                  unsigned index) {
  if (!ptr) _throw_null(); 
  if (index >= bound) _throw_arraybounds(); 
  return ptr + elt_sz*index; 
}

static void *_check_known_subscript_notnull(void *ptr,unsigned bound,unsigned elt_sz,
                                     unsigned index) {
  if (index >= bound) _throw_arraybounds(); 
  return (void *)((char *)ptr + elt_sz*index);
}

