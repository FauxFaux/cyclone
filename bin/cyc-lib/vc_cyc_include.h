static char *_check_fat_subscript(struct _fat_ptr arr, unsigned elt_sz, int index) {
  char *ans = arr.curr + elt_sz*index;
  if (ans < arr.base || ans >= arr.last_plus_one)
    _throw_arraybounds();
  return ans;
}

#define _untag_fat_ptr(arr,elt_sz,num_elts) ((arr).curr)

static char *_untag_fat_ptr_check_bound(struct _fat_ptr arr, unsigned elt_sz, unsigned num_elts) {
  char *curr = arr.curr;
  if ((curr < arr.base || curr + elt_sz*num_elts > arr.last_plus_one) &&
      curr != (char *)0)
    _throw_arraybounds();
  return curr;
}

static struct _fat_ptr _check_fat_at_base(struct _fat_ptr arr) {
  if (arr.base != arr.curr) _throw_arraybounds();
  return arr;
}

static struct _fat_ptr _tag_fat(void *tcurr, unsigned elt_sz, unsigned num_elts) {
  struct _fat_ptr ans;
  ans.base = ans.curr = (char *)tcurr;
  ans.last_plus_one = ans.base ? (ans.base + elt_sz*num_elts) : 0;
  return ans;
}

static unsigned _get_fat_size(struct _fat_ptr arr, unsigned elt_sz) {
  char *curr = arr.curr;
  char *last = arr.last_plus_one;
  if (curr < arr.base || curr >= last) return 0;
  return ((last - curr) / elt_sz);
}

static struct _fat_ptr _fat_ptr_plus(struct _fat_ptr arr, unsigned elt_sz, int change) {
  arr.curr += elt_sz * change;\
  return arr;
}

static struct _fat_ptr _fat_ptr_inplace_plus(struct _fat_ptr *arr_ptr, unsigned elt_sz, int change) {
  arr_ptr->curr += elt_sz * change;
  return *arr_ptr;
}

static struct _fat_ptr _fat_ptr_inplace_plus_post(struct _fat_ptr *arr_ptr, unsigned elt_sz, int change) {
  struct _fat_ptr ans = *arr_ptr;
  arr_ptr->curr += elt_sz * change;
  return ans;
}

